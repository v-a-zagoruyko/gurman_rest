const options = {
  index: 0,
  closeOnScroll: false,
};

const galleryContainer = document.querySelectorAll(".pswp")[0];
// const galleryToggle = document.getElementById("run-gallery");
const galleryToggle1 = document.getElementById("run-gallery-1");
const galleryToggle2 = document.getElementById("run-gallery-2");
const galleryToggle3 = document.getElementById("run-gallery-3");

galleryToggle1.addEventListener("click", (e) => {
  e.preventDefault();

  let gallery = new PhotoSwipe(
    galleryContainer,
    PhotoSwipeUI_Default,
    room1,
    options
  );
  gallery.listen("gettingData", function (index, item) {
    if (item.w < 1 || item.h < 1) {
      // unknown size
      var img = new Image();
      img.onload = function () {
        item.w = this.width;
        item.h = this.height;
        gallery.invalidateCurrItems();
        gallery.updateSize(true);
      };
      img.src = item.src;
    }
  });

  gallery.init();
});

